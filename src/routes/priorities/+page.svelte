<div class="text-column">
<h1>1. | Every Child Must Grow</h1>
<div class="section">I believe that each child should be appropriately supported and challenged in a learning environment that is suitable for their academic needs. I support providing teachers with the resources they need to effectively implement differentiated instruction and flexible grouping. In addition, my family and I are strong supporters of PAUSDâ€™s Choice Programs, from Ohlone to JLS Connections to Paly TEAM to Middle College High School. Our focus must be to ensure growth for every student in our district.  We need to stem the flow of students to private school. I will support expanding CTE pathways, dual enrollment, as well as opportunities in the humanities.
</div>
<h1>2. | Diverse Communities</h1>
<div class="section">As the child of immigrants, who was bullied, marginalized, and struggled economically during my school days, I am passionate about closing the achievement and opportunity gap, and championing equity, so that each and every student can fully realize their potential. 
</div>
<h1>3. | Mental Health</h1>
<div class="section">I have extensive experience in the field of mental health, sexual assault, and suicide prevention. I am deeply concerned about the current mental health crisis in this school district and plan to make communication, education, and evidence-based problem-solving an urgent priority. Listening to student voices is key to making meaningful change. I will work to improve safety for students and teachers and stronger partnership with Safe Routes to School to ensure that our children can safely walk or bike to school. Improving teacher morale is a priority.
</div>
<h1>4. | Special Education</h1>
<div class="section">I started out in local politics by advocating for the needs of our Special Education community. I am passionate about: de-stigmatizing SpEd, ensuring that our special education students and teachers are provided with the necessary resources and supports, and reducing litigation. 
</div>
<h1>5. | Transparency</h1>
<div class="section">I plan to utilize my degrees in business, corporate finance and law, as well as decades of experience in data analytics, to support PAUSD in budget decisions, offering the community a fair and transparent view of data around budget, performance, and tracking.  I will bring evidence-based policies to the district. I support using California Healthy Kids Survey data to ensure that every student feels a sense of belonging. I support using survey data to calibrate homework and assessments.
</div>
</div>
